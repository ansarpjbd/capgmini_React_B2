//Type Casting
//convert from one data type to another data type
//float -->int
//double --float---int
//String -->int 

//

public class MyTypeCasting {

	public static void main(String arg[])
	{
    float f=1.0f;
    //float--->int
    int a=(int)f;
    
    //double -->int
    double d=1.0;
    //explicit type casting
    int b=(int)d;
    
    //float -->f
    //implicit type casting
    
     //small  -- big 
    //Source --- >destination
    double dd=(double)f;
    
    System.out.println(a);
	}
	
}
